export default function I18nProvider({ children }: { children: React.ReactNode }) {
    const [lang, setLang] = useState<'en' | 'es' | 'jp'>('en');
    const t = translations[lang];
    return (
        <I18nContext.Provider value={{ lang, setLang, t }}>
            {children}
        </I18nContext.Provider>
    );
}